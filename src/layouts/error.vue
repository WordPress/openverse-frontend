<template>
  <main class="bg-yellow h-screen relative">
    <Logo class="pt-6 lg:pt-8 ms-6 lg:ms-10 w-30 lg:w-80 h-auto" />
    <Oops
      aria-hidden="true"
      class="absolute opacity-5 fill-dark-charcoal -mt-[10%] -ml-[20%] lg:mx-auto w-[140%] lg:w-full px-6 lg:px-16"
    />
    <header
      class="absolute lg:max-w-2xl space-y-4 top-1/4 z-10 left-0 right-0 mx-auto px-6 lg:px-0"
    >
      <h1 class="mb-6 lg:leading-tight text-4xl lg:text-[50px]">
        {{ $t('404.title') }}
      </h1>
      <p class="font-semibold">
        <i18n path="404.main">
          <template #link>
            <NuxtLink
              class="underline text-current hover:text-current active:text-current"
              to="/"
            >
              {{ $t('404.link-title') }}
            </NuxtLink>
          </template>
        </i18n>
      </p>
      <div class="bg-white">
        <VSearchBar
          :value="searchTerm"
          :label-text="$t('404.search-placeholder')"
          field-id="404-search"
          :placeholder="$t('404.search-placeholder')"
          @input="setSearchTerm"
          @submit="handleSearch"
        />
      </div>
    </header>
  </main>
</template>

<script>
import {
  defineComponent,
  ref,
  useContext,
  useRouter,
} from '@nuxtjs/composition-api'
import { MEDIA, SEARCH } from '~/constants/store-modules'
import { FETCH_MEDIA, UPDATE_QUERY } from '~/constants/action-types'

import Oops from '~/assets/oops.svg?inline'
import Logo from '~/assets/logo.svg?inline'
import VSearchBar from '~/components/VHeader/VSearchBar/VSearchBar'

const Error = defineComponent({
  name: 'ErrorPage',
  components: {
    Logo,
    Oops,
    VSearchBar,
  },
  layout: 'blank',
  props: ['error'],
  setup() {
    const { app, store } = useContext()
    const router = useRouter()

    const searchTerm = ref('')
    const setSearchTerm = (value) => {
      searchTerm.value = value
    }

    const handleSearch = async () => {
      await store.dispatch(`${SEARCH}/${UPDATE_QUERY}`, {
        q: searchTerm.value,
      })
      await store.dispatch(`${MEDIA}/${FETCH_MEDIA}`, {
        query: { q: searchTerm.value },
      })

      router.push(
        app.localePath({
          path: `/search`,
          query: { q: searchTerm.value },
        })
      )
    }

    return {
      searchTerm,
      setSearchTerm,
      handleSearch,
    }
  },
})
export default Error
</script>
