<template>
  <section
    :key="type"
    class="p-6 meta-search text-center mt-12"
    data-testid="meta-search-form"
  >
    <header class="mb-10">
      <i18n
        v-if="!hasNoResults"
        :path="
          isSupported
            ? 'meta-search.form.supported-title'
            : 'meta-search.form.unsupported-title'
        "
        tag="h4"
        class="b-header mb-2"
      >
        <!-- eslint-disable-next-line @intlify/vue-i18n/no-raw-text -->
        <template #openverse>Openverse</template>
        <template #type>
          {{ type }}
        </template>
      </i18n>
      <i18n
        v-else
        path="meta-search.form.no-results-title"
        tag="h4"
        class="b-header mb-2"
      >
        <template #type>{{ type }}</template>
        <template #query>{{ searchTerm }}</template>
      </i18n>
      <i18n path="meta-search.form.caption" tag="p">
        <template #type>{{ type }}</template>
        <template #break>
          <br />
        </template>
        <template #filter>{{ unsupportedByUsefilter }}</template>
      </i18n>
    </header>

    <VMetaSourceList
      class="md:justify-center mt-6 mb-10"
      :type="type"
      :query-params="metaQuery"
    />

    <p class="caption font-semibold max-w-3xl my-0 mx-auto">
      {{ $t('meta-search.caption', { openverse: 'Openverse' }) }}
    </p>
  </section>
</template>

<script>
import { AUDIO, IMAGE, VIDEO } from '~/constants/media'
import { useFilterStore } from '~/stores/filter'

import VMetaSourceList from './VMetaSourceList.vue'

export default {
  name: 'VMetaSearchForm',
  components: {
    VMetaSourceList,
  },
  props: {
    type: { type: String, required: true },
    searchTerm: { type: String, required: true },
    isSupported: { type: Boolean, default: false },
    hasNoResults: { type: Boolean, required: true },
  },
  computed: {
    licenseTypeFilters() {
      return useFilterStore().searchQueryParams?.license_type
    },
    unsupportedByUsefilter() {
      if (this.type === AUDIO) {
        return 'CC Mixter'
      }
      if (this.type === VIDEO) return 'Wikimedia Commons or Youtube'
      if (this.type === IMAGE) return 'Google Images'
      return ''
    },
    metaQuery() {
      return {
        q: this.searchTerm,
        filters: {
          commercial: this.licenseTypeFilters
            ? this.licenseTypeFilters.includes('commercial')
            : false,
          modify: this.licenseTypeFilters
            ? this.licenseTypeFilters.includes('modification')
            : false,
        },
      }
    },
  },
}
</script>
